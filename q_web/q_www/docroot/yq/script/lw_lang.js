function QueryString(uriStr){
  var sea =  (window.location.search).slice(1);
  var str = sea.split('&');
  var query;  
  for(var i =0 ; i < str.length; i++){
    query = str[i].split('=');
	if( query[0] == uriStr ) return query[1] ;
  }
  return ''	
}
